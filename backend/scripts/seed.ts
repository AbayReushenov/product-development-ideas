async function seedDatabase(): Promise<void> {
  try {
    console.log('üå± Starting database seeding...');

    // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ —É–∂–µ –¥–∞–Ω–Ω—ã–µ
    const existingIdeas = await pool.query('SELECT COUNT(*) FROM ideas');
    if (parseInt(existingIdeas.rows[0].count) > 0) {
      console.log('‚ö†Ô∏è  Database already contains data. Skipping seed.');
      return;
    }

    // –í—Å—Ç–∞–≤–ª—è–µ–º —Ç–µ—Å—Ç–æ–≤—ã–µ –¥–∞–Ω–Ω—ã–µ
    for (const idea of ideasData) {
      await pool.query(
        'INSERT INTO ideas (title, description) VALUES ($1, $2)',
        [idea.title, idea.description]
      );
    }

    console.log(`‚úÖ Successfully seeded ${ideasData.length} ideas`);
    console.log('üéâ Database seeding completed!');
  } catch (error) {
    console.error('‚ùå Seeding failed:', error);
    process.exit(1);
  } finally {
    await pool.end();
  }
}

seedDatabase();
